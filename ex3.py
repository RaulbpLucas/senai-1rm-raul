artes = float (input ("Informe sua nota de Artes: "))
ingles = float (input ("Informe sua nota de Ingles: "))
portugues = float (input("Informe sua nota de Portugues: "))
media = (artes + ingles + portugues) / 3
print ("Sua nota de Artes: {}. Sua nota de Ingles: {}. Sua nota de Portugues: {}. Sua media final: {}".format(artes,ingles,portugues,media))